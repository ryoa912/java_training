package jpl.ch09.ex01;

public class Calcurator {
	public static final float addFloat(float a, float b) {
		return a + b;
	}

	public static final float subFloat(float a, float b) {
		return a - b;
	}

	public static final float mulFloat(float a, float b) {
		return a * b;
	}

	public static final float divFloat(float a, float b) {
		return a / b;
	}

	public static final double addDouble(double a, double b) {
		return a + b;
	}

	public static final double subDouble(double a, double b) {
		return a - b;
	}

	public static final double mulDouble(double a, double b) {
		return a * b;
	}

	public static final double divDouble(double a, double b) {
		return a / b;
	}
}
